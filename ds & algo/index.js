/**
 * What is Data structure ?
 * A data structure is a way of organizing and storing data to perform operations efficiently. 
 * It defines the relationships and the rules governing the data, ensuring that data can be easily managed, accessed,
 * and modified. Data structures are fundamental in computer science and play a crucial role in algorithm design and optimization.
 *  The choice of an appropriate data structure often depends on the specific requirements and characteristics of the problem at hand.


There are two main types of data structures:

Primitive Data Structures:

These are the basic data structures that directly operate upon the machine instructions. Examples include integers,
 floating-point numbers, characters, and pointers. They are the building blocks for more complex data structures.


Composite Data Structures:

These are structures composed of primitive data types and can be further categorized into two main types:
Linear Data Structures: Elements are arranged in a linear sequence. Examples include arrays, linked lists, stacks, and queues.
Non-linear Data Structures: Elements are not arranged in a sequence. Examples include trees and graphs.

Common Data Structures:

Arrays:
A collection of elements, each identified by an index or a key.

Linked Lists:
Elements are stored in nodes, and each node points to the next one in the sequence.

Stacks:
Follows the Last In, First Out (LIFO) principle. Elements are added and removed from the same end.

Queues:
Follows the First In, First Out (FIFO) principle. Elements are added at one end and removed from the other.

Trees:
Hierarchical data structure with a root element and subtrees of children with a parent-child relationship.

Graphs:
A collection of nodes with edges connecting them. Graphs can be directed or undirected.

Hash Tables:
A data structure that implements an associative array abstract data type, where keys are mapped to values.
Understanding the characteristics and performance implications of different data structures is crucial for designing 
efficient algorithms and optimizing the performance of software systems. 
The choice of a particular data structure often depends on the requirements of the specific application and 
the operations that need to be performed on the data.
 */
